    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarActionPerformed
        Mysql cc = new Mysql();
            Connection cn = cc.conexion();
            String sql = "insert into REALIZA_PEDIDO values (?,?,?,?,?,?)";
        try{
            PreparedStatement pst = cn.prepareStatement(sql);
            pst.setString(4, txtTTarjeta.getText());
            pst.setString(1, txtNTarjeta.getText());
            pst.setString(2, txtCVC.getText());
            pst.setString(5, txtFCad.getText());
            pst.setString(3,Menu_Cliente.usuario );
            pst.setString(6,String.valueOf(totalAPagar));
            int n = pst.executeUpdate();
            if(n>0){
                JOptionPane.showMessageDialog(null, "Pago realizado con Exito!");
            }
        
        } catch (SQLException ex) {
            Logger.getLogger(Resgistrar_Clientes.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        sql="insert into Compone values (?,?)";
        
        try{
            PreparedStatement npst=cn.prepareStatement(sql);   // se supone que si el realiza pedido lo hace bien
                                                                  // este tampoco deberia petar
            npst.setString(1, Menu_Cliente.id_menu);
            npst.setString(2, txtNTarjeta.getText() );
            int n = npst.executeUpdate();
            if(n>0){
                JOptionPane.showMessageDialog(null, "Todo Correcto. Disfrute de su pedido.");
            }
            
        }catch(Exception e){}
        
        dispose();
    }//GEN-LAST:event_btnPagarActionPerformed

