    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
   
            Mysql cc = new Mysql();
            Connection cn = cc.conexion();
          
            String sql = "insert into CLIENTE values (?,?,?,?,?,?)";
            
        try{
            PreparedStatement pst = cn.prepareStatement(sql);
            pst.setString(1, txtNombre.getText());
            pst.setString(2, txtApellidos.getText());
            pst.setString(3, txtCorreo.getText());
            pst.setString(4, txtUsuario.getText());
            pst.setString(5, txtTelefono.getText());
            pst.setString(6, txtEdad.getText());
            int n = pst.executeUpdate();
            if(n>0){
                JOptionPane.showMessageDialog(null, "Registro guardado con Exito!");
                Bloquear();
            }
        
        } catch (SQLException ex) {
            Logger.getLogger(Resgistrar_Clientes.class.getName()).log(Level.SEVERE, null, ex);
        }     
    }//GEN-LAST:event_btnRegistrarActionPerformed

