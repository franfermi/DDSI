    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Mysql cc = new Mysql();
        Connection cn = cc.conexion();

        try {
            
            
            Statement pst= cn.createStatement();
            ResultSet rs=pst.executeQuery("select num_tarj, usuarioped, tipo_tarj, totalapagar from realiza_pedido");
            ResultSetMetaData md=rs.getMetaData();

            int ncolum=md.getColumnCount();

            System.out.println(ncolum);

            DefaultTableModel modelo=new DefaultTableModel();

            this.jTable1.setModel(modelo);

            for(int i=1;i<=ncolum;i++)
                   modelo.addColumn(md.getColumnLabel(i));   //titulos en la tabla

            while (rs.next())
            {
                Object [] fila= new Object[ncolum];

                for(int i=0;i<ncolum;i++)
                {
                    fila[i]=rs.getObject(i+1);   // aÃ±adimos elementos
                }

                modelo.addRow(fila);

            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton1ActionPerformed

